-- Очки за игру: расширяем точность с 1 до 2 знаков после запятой.
-- Источник «Летний кубок» содержит допы с точностью 0.05 (балл 1.05),
-- которые при DECIMAL(4,1) округлялись до 0.1. Также пригодится для тонких
-- баллов в обычных играх.
ALTER TABLE game_seats
    MODIFY plus  DECIMAL(5,2) NOT NULL DEFAULT 0,
    MODIFY minus DECIMAL(5,2) NOT NULL DEFAULT 0;
